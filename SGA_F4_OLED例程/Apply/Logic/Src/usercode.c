/* 用户逻辑代码 */
void UserLogic_Code(void)
{
	printf("SGA_DEMO\r\n");

    
	while(1)
	{
        /* OLED显示各种不同的数据 */
        OCD_OLED_ShowString(&tOLED,0,0,"OLED Test",16);
        OCD_OLED_ShowString(&tOLED,0,2,"Menu",8);
        OCD_OLED_ShowString(&tOLED,0,3,"Setting",8);
        OCD_OLED_ShowString(&tOLED,0,4,"Exit",16);
        OCD_OLED_ShowNum(&tOLED,0,6,12345678,8,8);
        OCD_OLED_ShowFloatNum(&tOLED,0,7,0.111,8);
        OCD_OLED_ShowChar(&tOLED,120,7,'F',8);
        Drv_Delay_Ms(1000);
        OCD_OLED_Clear(&tOLED);
        
        /* OLED显示BMP图片 */
        OCD_OLED_ShowBMP(&tOLED,0, 0,128,7,BMP);
        Drv_Delay_Ms(1000);
        OCD_OLED_Clear(&tOLED);
	}
}
